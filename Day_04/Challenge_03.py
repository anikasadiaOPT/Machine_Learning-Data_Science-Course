# The Stack Emulator

data = []               # an empty list
data.append(1)          # .append() -> add new data /value at the end of previous data
data.append(2)
data.append(3)
print(f"After adding all the numbers in a list named data\ndata = {data}")  # Ouptu: [1, 2, 3]

data.pop()              # remove the element from the last
print(f"After removing the last number from the list\ndata = {data}")        # Output: [1, 2]
